 class loops {
    public static void main(String[] args) {
        // java program implementing loops
        // while loop
        int i =1;
        while (i<=5) {
            System.out.println("hello "+i);
            int j = 1;
            while(j<=3){
                System.out.println("hry "+j);
                j++;
            }
            i++;
        }
        System.out.println("bye "+i);
    }
}
