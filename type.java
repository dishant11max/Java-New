public class type {
    public static void main(String[] args) {
        /* type casting
         * 
         * 
         * int a = 258;
         * byte b = (byte)a;
         * 
         * float f = 55.6f;
         * int a = (int)f;
         * 
         * 
         * 
         */
        // type promotion
        byte b1=20;
        byte b2=30;
        int result = b1*b2;
        System.out.println(result);
    }
}